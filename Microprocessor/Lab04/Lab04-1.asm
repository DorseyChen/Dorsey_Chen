ORG 0000H
JMP MAIN
ORG 0050H

MAIN:
	MOV R0,#0
	MOV R1,#010H
	MOV R2,#0
	MOV R3,#0
	MOV R4,#0
	MOV DPTR,#TABLE1
LOOP:
	MOV P1,#00H
	MOV A,R0
	MOVC A,@A+DPTR
	MOV P0,A
	MOV P1,R1
	INC R0
	MOV A,R1
	RR A
	MOV R1,A
	CALL DELAY
	CJNE R0,#5,LOOP
	MOV R0,#0
	MOV R1,#10H
	INC R2
	CJNE R2,#250,LOOP
	DEC R2
	MOV DPTR,#TABLE2
	INC R3
	CJNE R3,#250,LOOP
	DEC R3
	MOV DPTR,#TABLE3
	INC R4
	CJNE R4,#250,LOOP
	JMP MAIN
TABLE1:
	DB		12H
	DB		14H		
	DB		78H	
	DB		14H		
	DB		12H		
TABLE2:
	DB		1CH			
	DB		14H		
	DB		7FH	
	DB		14H		
	DB		1CH		
TABLE3:
	DB		08H			
	DB		12H		
	DB		7EH	
	DB		10H		
	DB		08H		
DELAY:
	MOV		R6,#50
DELAY1:
	MOV		R7,#50
DELAY2:
	DJNZ	R7,DELAY2
	DJNZ	R6,DELAY1
	RET
END