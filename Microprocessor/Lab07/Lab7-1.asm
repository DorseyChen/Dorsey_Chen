	ORG	0000H
	JMP	START
	ORG	0050H
START:
	MOV		A,#00010001B	//設定旋轉參數
	MOV 	R0,#0			//將R0設為一個度數參考
MAIN:
	MOV		P1,A			//輸出P1
	CALL	DELAY			//Delay讓馬達可以旋轉
	RR		A				//輪轉A讓參數方向不同	
	INC		R0				//R0++
	CJNE	R0,#75,MAIN		//R0到75則繼續執行
	MOV		R0,#0
MAIN1:
	MOV		P1,A			//增大R0的值改變角度
	CALL	DELAY
	RL		A
	INC		R0
	CJNE	R0,#150,MAIN1
	MOV		R0,#0
	JMP		MAIN
DELAY:
	MOV	R6,#200
DELAY1:
	MOV	R7,#250
DELAY2:
	DJNZ	R7,DELAY2
	DJNZ	R6,DELAY1
	RET
END